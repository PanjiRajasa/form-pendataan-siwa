    private void Panel_Siswa() {
        panel();
        Panel_Siswa.setVisible(true);

        String[] judul = {
            "NIS", "Nama", "Tempat Lahir", "Tanggal Lahir", "Agama",
            "Alamat", "Jenis Kelamin", "No Telepon", "Kelas", "Nama Guru"
        };

        DefaultTableModel tabel = new DefaultTableModel(judul, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tableSiswa.setModel(tabel);

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pendataan_siswa", "root", "");
            Statement stmt = conn.createStatement();

            String query = "SELECT s.nis, s.nama_lengkap, s.tmpt_lhr, s.tgl_lhr, s.agama, " +
                           "s.alamat, s.jenis_kelamin, s.no_hp, k.nama_kelas, g.nama_guru " +
                           "FROM tbl_siswa s " +
                           "JOIN tbl_kelas k ON s.id_kelas = k.id_kelas " +
                           "JOIN tbl_guru g ON k.id_guru = g.id_guru";

            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                String nis = rs.getString("nis");
                String nama = rs.getString("nama_lengkap");
                String tempatLahir = rs.getString("tmpt_lhr");
                String tanggalLahir = rs.getString("tgl_lhr");
                String agama = rs.getString("agama");
                String alamat = rs.getString("alamat");
                String jenisKelamin = rs.getString("jenis_kelamin");
                String noHp = rs.getString("no_hp");
                String namaKelas = rs.getString("nama_kelas");
                String namaGuru = rs.getString("nama_guru");

                String[] data = {
                    nis, nama, tempatLahir, tanggalLahir, agama,
                    alamat, jenisKelamin, noHp, namaKelas, namaGuru
                };

                tabel.addRow(data);
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat data siswa:\n" + e.getMessage());
        }
    }

