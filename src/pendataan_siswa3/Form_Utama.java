    private void EditOrHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditOrHapusActionPerformed
        // Hapus data
        String selectedAction = (String) EditOrHapus.getSelectedItem();
        
        if ("Hapus".equalsIgnoreCase(selectedAction)) {
            int selectedRow = tableGuru.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus.");
                return;
            }

        int confirm = JOptionPane.showConfirmDialog(null, 
            "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
                try {
                    
                    // Ambil ID guru dari kolom pertama (misalnya ID ada di kolom 0)
                    String idGuru = tableGuru.getValueAt(selectedRow, 0).toString();

                    // Buat koneksi dan query DELETE
                    Connection conn = DriverManager.getConnection(
                        "jdbc:mysql://localhost:3306/pendataan_siswa", "root", "");
                    String sql = "DELETE FROM tbl_guru WHERE id_guru = ?";
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, idGuru);
                    ps.executeUpdate();
                    
                    // Refresh table
                    loadTableData();

                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                    
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menghapus data.");
                }
          }
       }
        //Edit data
        else if ("Edit".equalsIgnoreCase(selectedAction)) { 
                int selectedRow = tableGuru.getSelectedRow();
                if (selectedRow == -1) {
                    JOptionPane.showMessageDialog(null, "Pilih baris yang ingin diedit.");
                    return;
                }
                
                // Ambil data dari tabel
                String id = tableGuru.getValueAt(selectedRow, 0).toString();
                String nama = tableGuru.getValueAt(selectedRow, 1).toString();
                String agama = tableGuru.getValueAt(selectedRow, 2).toString();
                String alamat = tableGuru.getValueAt(selectedRow, 3).toString();
                String jenis_kelamin = tableGuru.getValueAt(selectedRow, 4).toString();
                String no_hp = tableGuru.getValueAt(selectedRow, 5).toString();
                
                //buka dialog
                EditTeacher editDialog = new EditTeacher(this, true); //true = modal
                editDialog.setLocationRelativeTo(this); //tampil di tengah
                editDialog.setData(id, nama, agama, alamat, jenis_kelamin, no_hp);
                
                editDialog.setVisible(true); // <- WAJIB agar dialog muncul
                
                // Refresh tabel setelah dialog ditutup
                loadTableData();

                // Reset comboBox
                EditOrHapus.setSelectedIndex(0);
         }
    }//GEN-LAST:event_EditOrHapusActionPerformed
    
    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) tableGuru.getModel();
        model.setRowCount(0); // Hapus semua baris
        
        try {
        Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/pendataan_siswa", "root", "");
        String sql = "SELECT * FROM tbl_guru";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
                Object[] row = {
                    rs.getString("id_guru"),
                    rs.getString("nama_guru"),
                    rs.getString("agama"),
                    rs.getString("alamat"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("no_hp"),
                };
                model.addRow(row);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal memuat data.");
        }
    }
    
